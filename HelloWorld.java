//: 주석
public class HelloWorld {
	// {} 중가로: 범위(영역,지역) 역할
	/* 여려 줄 주석
	 * ....
	 */
    public static void main(String[] args) {
    	// main: 자바 프로글 실행시 필수 없으면 실행 안됨
    	System.out.println("Hello World");
    	//()소가로 안쪽 무언가 출력 가능 "" 문자열 출력시 필요
    	//; - 한 줄의 코드(문장)다 쓰면 마침표 찍는 것 처럼 찍어야함(중가로에선 안씀)
   
    }
}
